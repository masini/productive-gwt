package org.googlecode.gwt.bootstrap.server.exception;

/**
 * Exception throw from EJB module
 *
 */
public class BootstrapModelException extends RuntimeException {

	private static final long serialVersionUID = -1262117585068028306L;
	
	public BootstrapModelException() {
		super();
	}

	public BootstrapModelException(String message, Throwable cause) {
		super(message, cause);
	}

	public BootstrapModelException(String message) {
		super(message);
	}

	public BootstrapModelException(Throwable cause) {
		super(cause);
	}

	
	
}
